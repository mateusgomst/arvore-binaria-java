    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        
        ArvoreBinaria arvoreAlunos = new ArvoreBinaria();
        String opcao = (String)jComboBox1.getSelectedItem();
        
        String alunoNomeOuMatricula; 
        
        
        for (Alunos aluno : listaAlunos) {
            try {
                
                if (opcao.compareTo("matricula") == 0) alunoNomeOuMatricula = aluno.getMatricula();        
                else alunoNomeOuMatricula = aluno.getNome(); 
                
                arvoreAlunos.incluir(alunoNomeOuMatricula, aluno); 
                //System.out.println("Aluno inclu√≠do: " + aluno.getNome());
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Erro ao incluir aluno: " + e.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
            }
        }

        try {
            List<Alunos> alunosEncontrados = arvoreAlunos.chamarOBuscarPorNomeOuMatricula(arvoreAlunos.getRaiz(), jTextFieldBusca.getText());
                if (alunosEncontrados.isEmpty()) {
                    JOptionPane.showMessageDialog(this, "Nenhum aluno encontrado com o valor especificado.", "Aviso", JOptionPane.INFORMATION_MESSAGE);
                } else {
                    
                    DefaultTableModel modeloTabela = (DefaultTableModel) jTable1.getModel();
                    modeloTabela.setRowCount(0); 

                    for (Alunos aluno : alunosEncontrados) {
                        Object[] rowData = {
                            aluno.getMatricula(),
                            aluno.getNome(),
                            aluno.getTurno(),
                            aluno.getPeriodo(),
                            aluno.getEnfase(),
                            aluno.getCurso()
                        };
                        modeloTabela.addRow(rowData);
                    }
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, e.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
            }

        
        
    }//GEN-LAST:event_jButton1ActionPerformed

