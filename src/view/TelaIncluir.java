    private void jButtonIncluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonIncluirActionPerformed
        
        try {
            
            Alunos aluno = new Alunos(
                jTextFieldMatricula.getText(),
                jTextFieldNome.getText(),
                jTextFieldTurno.getText(),
                Integer.parseInt(jTextFieldPeriodo.getText()),
                jTextFieldEnfase.getText(),
                jTextFieldCurso.getText()
            );

            
            if(listaAlunos != null) {
                listaAlunos.add(aluno);
                JOptionPane.showMessageDialog(this, "Aluno adicionado com sucesso!");
            } 

            jTextFieldMatricula.setText("");
            jTextFieldNome.setText("");
            jTextFieldPeriodo.setText("");
            jTextFieldEnfase.setText("");
            jTextFieldTurno.setText("");
            jTextFieldCurso.setText("");

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Erro: O valor do Periodo deve ser um n√∫mero inteiro.");
        }
        
        dispose();
    }//GEN-LAST:event_jButtonIncluirActionPerformed

